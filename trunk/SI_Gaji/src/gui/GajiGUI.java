    private void insert_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insert_btnActionPerformed
        // TODO add your handling code here:
        String nama;
        String golongan;
        double gajiPokok;
        double pajak;
        double tunjangan1 = 0;
        double tunjangan2 = 0;
        String bulan;
        double gajiBersih;

        try {

            if (nama_txt.getText().equals("") || golongan_txt.getText().equals("")
                    || gajipokok_txt.getText().equals("") || pajak_txt.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Data Kosong");
            } else {

                if (tunjangan1_txt.getText().equals("")) {
                    tunjangan1 = 0;
                }

                if (tunjangan2_txt.getText().equals("")) {
                    tunjangan2 = 0;
                }
                
                else{
                    tunjangan1 = Double.parseDouble(tunjangan1_txt.getText());
                    tunjangan2 = Double.parseDouble(tunjangan2_txt.getText());
                }

                nama = nama_txt.getText();
                golongan = golongan_txt.getText();
                gajiPokok = Double.parseDouble(gajipokok_txt.getText());
                pajak = Double.parseDouble(pajak_txt.getText());
                
                bulan = bulan_cmb.getSelectedItem().toString();

                gajiBersih = (gajiPokok + tunjangan1 + tunjangan2) - ((pajak / 100) * gajiPokok);
                
                tbGaji gaji = new tbGaji();
                gaji.setNama(nama);
                gaji.setGolongan(golongan);
                gaji.setGajiPokok(gajiPokok);
                gaji.setPajak(pajak);
                gaji.setTunjangan1(tunjangan1);
                gaji.setTunjangan2(tunjangan2);
                gaji.setBulan(bulan);
                gaji.setGajiBersih(gajiBersih);
                
                gajiDAO.insert(gaji);
                
            }

            this.bindingGaji();
            this.bindingKaryawan();


        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_insert_btnActionPerformed

