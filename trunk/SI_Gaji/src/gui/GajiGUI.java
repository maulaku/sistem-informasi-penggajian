    public void bindingKaryawan() {

        try {
            String[] title = {"ID", "Nama", "Golongan"};
            Object[][] data = new Object[kDAO.getAll().size()][3];

            int i = 0;
            while (i < kDAO.getAll().size()) {
                data[i][0] = kDAO.getAll().get(i).getId();
                data[i][1] = kDAO.getAll().get(i).getNama();
                data[i][2] = kDAO.getAll().get(i).getGolongan();
                i++;
            }

            DefaultTableModel model = new DefaultTableModel(data, title);
            tabelkaryawan.setModel(model);
            this.jScrollPane1.setViewportView(tabelkaryawan);
        } catch (Exception e) {
            e.getMessage();
        }
    }

    public void bindingGaji() {
        try {
            String[] title = {"ID", "Nama", "Golongan", "Gaji Pokok", "Tunjangan 1", "Tunjangan 2", "Pajak", "Gaji Bersih", "Bulan"};
            Object[][] data = new Object[gajiDAO.getAll().size()][9];

            int i = 0;
            while (i < gajiDAO.getAll().size()) {
                data[i][0] = gajiDAO.getAll().get(i).getId();
                data[i][1] = gajiDAO.getAll().get(i).getNama();
                data[i][2] = gajiDAO.getAll().get(i).getGolongan();
                data[i][3] = gajiDAO.getAll().get(i).getGajiPokok();
                data[i][4] = gajiDAO.getAll().get(i).getTunjangan1();
                data[i][5] = gajiDAO.getAll().get(i).getTunjangan2();
                data[i][6] = gajiDAO.getAll().get(i).getPajak();
                data[i][7] = gajiDAO.getAll().get(i).getGajiBersih();
                data[i][8] = gajiDAO.getAll().get(i).getBulan();
                i++;
            }

            DefaultTableModel model = new DefaultTableModel(data, title);
            tabelgaji.setModel(model);
            //tabelgaji.setEnabled(false);
            //tabelgaji.setDragEnabled(false);
            this.jScrollPane2.setViewportView(tabelgaji);
        } catch (Exception e) {
            e.getMessage();
        }
    }

